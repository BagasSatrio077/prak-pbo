class BagasSatrio:

    def __init__(self,nama,nim,pbo,jumsks):
        self.nama = nama
        self.nim = nim
        self.pbo = pbo
        self.jumsks = jumsks
    def mahasiswa(self):
        print ("Nama = ", self.nama)
        print ("NIM = ", self.nim)
        print ("Kelas PBO = ", self.pbo)
        print ("Jumlah SKS = ", self.jumsks)
        
